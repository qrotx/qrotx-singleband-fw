/// Cascaded biquad filter, N sections, f32.
///
/// Coefficients per section: `[b0, b1, b2, -a1, -a2]` (a-coefficients sign-negated).
pub struct BiquadState<const N: usize> {
    s: [[f32; 2]; N],
}

impl<const N: usize> BiquadState<N> {
    pub const fn new() -> Self {
        Self { s: [[0.0f32; 2]; N] }
    }

    /// Process `input` through all N sections into `output` (slice lengths must match).
    pub fn process(
        &mut self,
        coeffs: &[[f32; 5]; N],
        input:  &[f32],
        output: &mut [f32],
    ) {
        debug_assert_eq!(input.len(), output.len());
        for (i, &x) in input.iter().enumerate() {
            let mut y = x;
            for (s, c) in self.s.iter_mut().zip(coeffs.iter()) {
                let [b0, b1, b2, na1, na2] = *c;
                let xin = y;
                y     = b0 * xin + s[0];
                s[0]  = b1 * xin + na1 * y + s[1];
                s[1]  = b2 * xin + na2 * y;
            }
            output[i] = y;
        }
    }
}

