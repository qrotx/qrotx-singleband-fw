    #[test]
    fn test_fir_decimate_passband_1khz() {
        // A 1 kHz sine at 200 kHz should pass through with near-unity gain.
        // Run 10 frames (1000 input → 100 output samples); skip first frame for settling.
        let input = sine_q31(1_000.0, 200_000.0, 0.5, FRAME_SAMPLES * 10);
        let mut dec = FirDecimate::new();
        let mut output = Vec::new();
        for chunk in input.chunks_exact(FRAME_SAMPLES) {
            let arr: [i32; FRAME_SAMPLES] = chunk.try_into().unwrap();
            output.extend_from_slice(&dec.process(&arr));
        }
        // Discard first DECIMATED_LEN output samples (filter settling).
        let out_rms   = rms_q31(&output[DECIMATED_LEN..]);
        let in_rms    = rms_q31(&input[FRAME_SAMPLES..]);
        // Gain should be within ±3 dB of unity.
        assert!(out_rms > in_rms * 0.7, "passband 1 kHz too attenuated: in={in_rms:.4} out={out_rms:.4}");
        assert!(out_rms < in_rms * 1.4, "passband 1 kHz amplified: in={in_rms:.4} out={out_rms:.4}");
    }

