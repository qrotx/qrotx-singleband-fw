    #[test]
    fn test_compressor_passes_quiet_signal() {
        // Signal well below threshold: gain = 1, makeup applied, output ≈ input × makeup.
        let amp = 0.1f32; // below COMP_THRESHOLD = 0.3
        let input = vec![amp; 5000];
        let mut comp = Compressor::new();
        let mut output = vec![0.0f32; 5000];
        comp.process(&input, &mut output);
        // After settling, gain = 1.0 and output ≈ input * COMP_MAKEUP_GAIN.
        let expected = (amp * COMP_MAKEUP_GAIN).min(1.0);
        let tail_rms = rms_f32(&output[1000..]);
        assert!((tail_rms - expected).abs() < 0.01,
            "Quiet signal: expected≈{expected:.3} got≈{tail_rms:.3}");
    }

