/// Complex analytic FIR filter producing I and Q from a real audio signal.
///
/// Shares a single 257-sample shift register for both paths.
pub struct SsbFilter {
    state: [f32; SSB_FILTER_TAPS],
}

impl SsbFilter {
    pub const fn new() -> Self {
        Self { state: [0.0f32; SSB_FILTER_TAPS] }
    }

    /// Process `input` into `i_out` and `q_out` (all slice lengths must match).
    pub fn process(
        &mut self,
        input: &[f32],
        i_out: &mut [f32],
        q_out: &mut [f32],
    ) {
        debug_assert_eq!(input.len(), i_out.len());
        debug_assert_eq!(input.len(), q_out.len());
        for k in 0..input.len() {
            self.state.copy_within(0..SSB_FILTER_TAPS - 1, 1);
            self.state[0] = input[k];

            i_out[k] = SSB_REAL_COEFFS
                .iter()
                .zip(self.state.iter().step_by(2))
                .map(|(&c, &x)| c * x)
                .sum();

            q_out[k] = SSB_IMAG_COEFFS
                .iter()
                .zip(self.state[1..].iter().step_by(2))
                .map(|(&c, &x)| c * x)
                .sum();
        }
    }
}

