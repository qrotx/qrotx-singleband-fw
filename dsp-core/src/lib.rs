    #[test]
    fn test_fir_interpolate_round_trip_rms() {
        // Decimate then interpolate a 1 kHz sine.  The round-trip should
        // roughly preserve signal energy (within 3 dB).
        let input = sine_q31(1_000.0, 200_000.0, 0.5, FRAME_SAMPLES * 20);
        let mut dec = FirDecimate::new();
        let mut interp = FirInterpolate::new();
        let mut output = vec![0i32; FRAME_SAMPLES * 20];

        for (frame, chunk) in input.chunks_exact(FRAME_SAMPLES).enumerate() {
            let arr_in: [i32; FRAME_SAMPLES] = chunk.try_into().unwrap();
            let decimated = dec.process(&arr_in);
            let mut arr_out = [0i32; FRAME_SAMPLES];
            interp.process(&decimated, &mut arr_out);
            output[frame * FRAME_SAMPLES..(frame + 1) * FRAME_SAMPLES]
                .copy_from_slice(&arr_out);
        }

        // Skip first two frames for filter settling.
        let in_rms  = rms_q31(&input[FRAME_SAMPLES * 2..]);
        let out_rms = rms_q31(&output[FRAME_SAMPLES * 2..]);
        assert!(out_rms > in_rms * 0.5, "Round-trip lost too much energy: in={in_rms:.4} out={out_rms:.4}");
        assert!(out_rms < in_rms * 2.0, "Round-trip amplified: in={in_rms:.4} out={out_rms:.4}");
    }

