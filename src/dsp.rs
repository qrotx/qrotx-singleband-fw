/// Convert a 12-bit right-aligned ADC sample (0…4095) to Q31.
#[inline]
pub fn adc_to_q31(sample: u16) -> i32 {
    (sample as i32 - 2048) << 19
}

/// Convert a Q31 sample to normalised f32 in [−1.0, 1.0).
#[inline]
pub fn q31_to_f32(sample: i32) -> f32 {
    sample as f32 * (1.0 / 2_147_483_648.0_f32)
}

/// Convert a normalised f32 in [−1.0, 1.0] to Q31, saturating at limits.
#[inline]
pub fn f32_to_q31(sample: f32) -> i32 {
    let v = sample * 2_147_483_648.0_f32;
    if v >= i32::MAX as f32 { i32::MAX }
    else if v <= i32::MIN as f32 { i32::MIN }
    else { v as i32 }
}

