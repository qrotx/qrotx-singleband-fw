// ---------------------------------------------------------------------------
// Compressor
// ---------------------------------------------------------------------------

const COMP_THRESHOLD:    f32 = 0.3;
const COMP_RATIO:        f32 = 4.0;
const COMP_MAKEUP_GAIN:  f32 = 1.5;
const COMP_ATTACK_COEFF: f32 = 0.22119922_f32;   // 1 − exp(−1 / (20000 × 0.0002))
const COMP_RELEASE_COEFF: f32 = 0.00049987503_f32; // 1 − exp(−1 / (20000 × 0.1))

pub struct Compressor {
    envelope: f32,
    gain:     f32,
}

impl Compressor {
    pub const fn new() -> Self {
        Self { envelope: 0.0, gain: 1.0 }
    }

    pub fn process(&mut self, input: &[f32], output: &mut [f32]) {
        for (out, &sample) in output.iter_mut().zip(input.iter()) {
            let abs_s = if sample < 0.0 { -sample } else { sample };
            if abs_s > self.envelope {
                self.envelope += COMP_ATTACK_COEFF  * (abs_s - self.envelope);
            } else {
                self.envelope += COMP_RELEASE_COEFF * (abs_s - self.envelope);
            }
            if self.envelope > COMP_THRESHOLD {
                let overshoot  = self.envelope / COMP_THRESHOLD;
                let compressed = 1.0 + (overshoot - 1.0) / COMP_RATIO;
                self.gain      = COMP_THRESHOLD * compressed / self.envelope;
            } else {
                self.gain = 1.0;
            }
            let s = sample * self.gain * COMP_MAKEUP_GAIN;
            *out = if s > 1.0 { 1.0 } else if s < -1.0 { -1.0 } else { s };
        }
    }
}

