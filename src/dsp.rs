///   • Write I[0],Q[0]           → launches calc[0]
///   • Write I[1],Q[1]           → queues  calc[1]
///   • Loop: read mod[i]+phase[i] → triggers calc[i+1]; write I[i+2],Q[i+2]
///   • Drain last two results
///
/// Reading CORDIC_RDATA inserts bus wait-states until the result is ready, so
/// there is no polling loop and no idle cycles between successive calculations.
///
/// CORDIC_CSR must already be configured for MODULUS, NARGS=1, NRES=1 (Q1.31).
pub unsafe fn cordic_modulus_vec(
    i_vals:    &[i32],
    q_vals:    &[i32],
    mod_out:   &mut [i32],
    phase_out: &mut [i32],
) {
    let n = i_vals.len();
    debug_assert!(n >= 1);

    let c = pac::CORDIC;

    if n == 1 {
        c.wdata().write_value(i_vals[0] as u32);
        c.wdata().write_value(q_vals[0] as u32);
        mod_out[0]   = c.rdata().read() as i32;
        phase_out[0] = c.rdata().read() as i32;
        return;
    }

    // Prime: launch calc[0], queue calc[1].
    c.wdata().write_value(i_vals[0] as u32);
    c.wdata().write_value(q_vals[0] as u32); // launches calc[0]
    c.wdata().write_value(i_vals[1] as u32);
    c.wdata().write_value(q_vals[1] as u32); // queues  calc[1]

    // Pipelined drain: read calc[i], queue calc[i+2].
    for i in 0..n - 2 {
        mod_out[i]   = c.rdata().read() as i32; // stalls until calc[i] done
        phase_out[i] = c.rdata().read() as i32; // triggers calc[i+1]
        c.wdata().write_value(i_vals[i + 2] as u32);
        c.wdata().write_value(q_vals[i + 2] as u32); // queues calc[i+2]
    }

    // Drain calc[n-2] (running; calc[n-1] already queued by last loop iteration).
    mod_out[n - 2]   = c.rdata().read() as i32;
    phase_out[n - 2] = c.rdata().read() as i32; // triggers calc[n-1]

    // Drain calc[n-1].
    mod_out[n - 1]   = c.rdata().read() as i32;
    phase_out[n - 1] = c.rdata().read() as i32;
}

// ---------------------------------------------------------------------------
// Per-stage timing — always collected; ~11 volatile reads overhead (~11 cy).
// Fields are DWT CYCCNT deltas; meaningful only when DWT is enabled.
// ---------------------------------------------------------------------------

/// Per-stage cycle counts returned by every `process_first/second_half()` call.
/// All fields are DWT CYCCNT deltas.  Enable DWT (TRCENA + CYCCNTENA) to get
/// real numbers; if DWT is off all fields are zero.  The DSP task drops the
/// return value so the compiler eliminates the dead arithmetic at -Ofast.
pub struct PipelineTimings {
    pub stage1_adc_to_q31:   u32,  // u16 → Q31 conversion (100 samples)
    pub stage2_fir_decimate:  u32,  // 10:1 FIR decimation (CMSIS-DSP Q31)
    pub stage3a_q31_to_f32:  u32,  // Q31 → f32 conversion (10 samples)
    pub stage3b_highpass:    u32,  // Chebyshev I HP biquad (CMSIS-DSP f32)
    pub stage3c_compress:    u32,  // Envelope-follower compressor (Rust)
    pub stage3d_lowpass:     u32,  // Chebyshev I LP biquad (CMSIS-DSP f32)
    pub stage4_ssb:          u32,  // 257-tap analytic FIR I+Q (C arm_fir_ssb_f32)
    pub stage5a_f32_to_q31:  u32,  // f32 → Q31 conversion (I and Q, 10 samples each)
    pub stage5b_interpolate:  u32,  // 10:1 FIR interpolation I+Q (CMSIS-DSP Q31)
    pub stage5c_cordic:       u32,  // CORDIC MODULUS vector (zero-overhead, 100 samples)
    pub stage5c_outphasing:   u32,  // outphasing: mod/phase → PwmSample (100 samples)
    pub total:                u32,  // wall-clock total (includes all overhead)
}

/// Read DWT CYCCNT directly.
#[inline(always)]
fn cyccnt() -> u32 {
    unsafe { (0xE000_1004 as *const u32).read_volatile() }
}

// ---------------------------------------------------------------------------
// Public pipeline entry points — called from the DSP Embassy task
// ---------------------------------------------------------------------------

/// Process the first half of the ADC ping-pong buffer (samples 0..FRAME_SAMPLES).
///
/// Returns per-stage DWT cycle counts; the DSP task drops them.  Enable DWT
/// before calling if the timing data is needed (see test_dsp_timing).
///
/// # Safety
/// Must be called only from the context that owns the first buffer half.
pub unsafe fn process_first_half() -> PipelineTimings {
    let adc = adc_buf_first_half();
    let hrtim_out = hrtim_buf_first_half_mut();
    process_half(adc, hrtim_out)
}

/// Process the second half of the ADC ping-pong buffer.
///
/// # Safety
/// Must be called only after the DMA transfer-complete flag has been set.
pub unsafe fn process_second_half() -> PipelineTimings {
    let adc = adc_buf_second_half();
    let hrtim_out = hrtim_buf_second_half_mut();
    process_half(adc, hrtim_out)
}

// ---------------------------------------------------------------------------
// Internal pipeline implementation
// ---------------------------------------------------------------------------

#[allow(static_mut_refs)]
unsafe fn process_half(adc_in: &[u16], hrtim_out: &mut [PwmSample]) -> PipelineTimings {
    debug_assert_eq!(adc_in.len(),    FRAME_SAMPLES);
    debug_assert_eq!(hrtim_out.len(), FRAME_SAMPLES);

    // --- Stage 1: ADC u16 → Q31 (DC centred) ---
    let t_start = cyccnt();
    let mut q31_in = [0i32; FRAME_SAMPLES];
    for (dst, &src) in q31_in.iter_mut().zip(adc_in.iter()) {
        *dst = adc_to_q31(src);
    }
    let t1 = cyccnt();

    // --- Stage 2: 10:1 FIR decimation → 10 Q31 samples at 20 kHz ---
    let mut decimated = [0i32; DECIMATED_LEN];
    arm_fir_decimate_fast_q31(
        &FIR_DEC_INST,
        q31_in.as_ptr(),
        decimated.as_mut_ptr(),
        FRAME_SAMPLES as u32,
    );
    let t2 = cyccnt();

    // --- Stage 3a: Q31 → f32 ---
    let mut audio = [0.0f32; DECIMATED_LEN];
    for (dst, &src) in audio.iter_mut().zip(decimated.iter()) {
        *dst = q31_to_f32(src);
    }
    let t3a = cyccnt();

    // --- Stage 3b: highpass IIR (Chebyshev I, 200 Hz) ---
    let mut hp_out = [0.0f32; DECIMATED_LEN];
    arm_biquad_cascade_df2T_f32(
        &BIQUAD_HP_INST,
        audio.as_ptr(),
        hp_out.as_mut_ptr(),
        DECIMATED_LEN as u32,
    );
    let t3b = cyccnt();

    // --- Stage 3c: compression ---
    let mut compressed = [0.0f32; DECIMATED_LEN];
    COMPRESSOR.process(&hp_out, &mut compressed);
    let t3c = cyccnt();

    // --- Stage 3d: lowpass IIR (Chebyshev I, 2800 Hz) ---
    let mut lp_out = [0.0f32; DECIMATED_LEN];
    arm_biquad_cascade_df2T_f32(
        &BIQUAD_LP_INST,
        compressed.as_ptr(),
        lp_out.as_mut_ptr(),
        DECIMATED_LEN as u32,
    );
    let t3d = cyccnt();

    // --- Stage 4: SSB analytic FIR → I and Q paths ---
    let mut ssb_i = [0.0f32; DECIMATED_LEN];
    let mut ssb_q = [0.0f32; DECIMATED_LEN];
    arm_fir_ssb_f32(
        &SSB_INST,
        lp_out.as_ptr(),
        ssb_i.as_mut_ptr(),
        ssb_q.as_mut_ptr(),
        DECIMATED_LEN as u32,
    );
    let t4 = cyccnt();

    // --- Stage 5a: f32 → Q31 ---
    let mut q31_i = [0i32; DECIMATED_LEN];
    let mut q31_q = [0i32; DECIMATED_LEN];
    for (dst, &src) in q31_i.iter_mut().zip(ssb_i.iter()) {
        *dst = f32_to_q31(src);
    }
    for (dst, &src) in q31_q.iter_mut().zip(ssb_q.iter()) {
        *dst = f32_to_q31(src);
    }
    let t5a = cyccnt();

    // --- Stage 5b: 10:1 FIR interpolation → 100 Q31 samples at 200 kHz ---
    let mut interp_i = [0i32; FRAME_SAMPLES];
    let mut interp_q = [0i32; FRAME_SAMPLES];
    arm_fir_interpolate_q31(
        &FIR_INTERP_I_INST,
        q31_i.as_ptr(),
        interp_i.as_mut_ptr(),
        DECIMATED_LEN as u32,
    );
    arm_fir_interpolate_q31(
        &FIR_INTERP_Q_INST,
        q31_q.as_ptr(),
        interp_q.as_mut_ptr(),
        DECIMATED_LEN as u32,
    );
    let t5b = cyccnt();

    // --- Stage 5c: CORDIC polar conversion + outphasing → HRTIM compare values ---
    let mut cordic_mod   = [0i32; FRAME_SAMPLES];
    let mut cordic_phase = [0i32; FRAME_SAMPLES];
    cordic_modulus_vec(&interp_i, &interp_q, &mut cordic_mod, &mut cordic_phase);
    let t5c = cyccnt();

    // All intermediate values stay within i32 (PWM_PERIOD=744, AMPLITUDE=425),
    // so i64 is not needed.  Modulo by a non-power-of-2 constant is replaced
    // with a single conditional subtract, which is cheaper than the compiler's
    // reciprocal-multiply trick.
    let half = PWM_PERIOD as i32 / 2; // 372
    for (i, slot) in hrtim_out.iter_mut().enumerate() {
        let modulus = cordic_mod[i];
        let phase   = cordic_phase[i];

        // CORDIC MODULUS output is non-negative Q1.31; scale to amplitude ticks.
        // Compensate for the 1/L gain of the CMSIS polyphase interpolator.
        let mag = ((modulus >> 15) * (AMPLITUDE as i32 * FIR_DECIMATE_FACTOR as i32)) >> 16;
        let tc_cmp1 = (mag as u32).clamp(3, AMPLITUDE);

        // Shift before negating to avoid i32::MIN overflow on negation.
        // phase_ticks ∈ [0, PWM_PERIOD]; conditional subtract instead of %.
        let phase_ticks = (-(phase >> 15) * half >> 16) + half;
        let ta_cmp1 = if phase_ticks >= PWM_PERIOD as i32 {
            (phase_ticks - PWM_PERIOD as i32) as u32
        } else {
            phase_ticks as u32
        };
        // ta_cmp1 + half ∈ [372, 1115], so one conditional subtract suffices.
        let t = ta_cmp1 + PWM_PERIOD / 2;
        let ta_cmp2 = if t >= PWM_PERIOD { t - PWM_PERIOD } else { t };

        *slot = PwmSample {
            tim_a_cmp1: ta_cmp1,
            tim_a_cmp2: ta_cmp2,
            tim_b_cmp1: ta_cmp1,
            tim_b_cmp2: ta_cmp2,
            tim_c_cmp1: tc_cmp1,
        };
    }
    let t_end = cyccnt();

    PipelineTimings {
        stage1_adc_to_q31:   t1.wrapping_sub(t_start),
        stage2_fir_decimate:  t2.wrapping_sub(t1),
        stage3a_q31_to_f32:  t3a.wrapping_sub(t2),
        stage3b_highpass:    t3b.wrapping_sub(t3a),
        stage3c_compress:    t3c.wrapping_sub(t3b),
        stage3d_lowpass:     t3d.wrapping_sub(t3c),
        stage4_ssb:          t4.wrapping_sub(t3d),
        stage5a_f32_to_q31:  t5a.wrapping_sub(t4),
        stage5b_interpolate: t5b.wrapping_sub(t5a),
        stage5c_cordic:      t5c.wrapping_sub(t5b),
        stage5c_outphasing:  t_end.wrapping_sub(t5c),
        total:               t_end.wrapping_sub(t_start),
    }
}
