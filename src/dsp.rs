/// Cascaded biquad filter with N sections, f32 arithmetic.
///
/// Each section stores coefficients as `[b0, b1, b2, -a1, -a2]` (normalised
/// by a0, a coefficients sign-negated so the update is a pure accumulate).
/// State per section: two delay elements `[s0, s1]`.
struct BiquadState<const N: usize> {
    s: [[f32; 2]; N],
}

impl<const N: usize> BiquadState<N> {
    const fn new() -> Self {
        Self { s: [[0.0f32; 2]; N] }
    }

    /// Process `input` through all N biquad sections in-place into `output`.
    ///
    /// Direct Form II Transposed update per section:
    ///   y    = b0·x + s0
    ///   s0  ← b1·x + na1·y + s1
    ///   s1  ← b2·x + na2·y
    fn process(
        &mut self,
        coeffs: &[[f32; 5]; N],
        input: &[f32; DECIMATED_LEN],
        output: &mut [f32; DECIMATED_LEN],
    ) {
        for (i, &x) in input.iter().enumerate() {
            let mut y = x;
            for (s, c) in self.s.iter_mut().zip(coeffs.iter()) {
                let [b0, b1, b2, na1, na2] = *c;
                let xin = y;
                y     = b0 * xin + s[0];
                s[0]  = b1 * xin + na1 * y + s[1];
                s[1]  = b2 * xin + na2 * y;
            }
            output[i] = y;
        }
    }
}

