/// Single-channel FIR interpolation filter (20 kHz → 200 kHz, factor 10), Q31.
///
/// Shares the same 60-tap Q31 prototype as the decimator, decomposed into 10
/// polyphase sub-filters of 6 taps each.  All arithmetic uses i64 accumulators
/// so that LLVM emits SMULL/SMLAL on Cortex-M4.
///
/// Polyphase output for input x[k], phase p (0..9):
///   y[k×10 + p] = 10 × Σ h[p + n×10] × x[k−n]   for n = 0..5
///
/// The factor 10 compensates for the 1/L power loss from zero-insertion.
/// Output is saturated to i32 Q31.
struct FirInterpolate {
    /// Last FIR_SUBFILTER_TAPS−1 = 5 input samples, oldest at index 0.
    state: [i32; FIR_SUBFILTER_TAPS - 1],
}

impl FirInterpolate {
    const fn new() -> Self {
        Self { state: [0i32; FIR_SUBFILTER_TAPS - 1] }
    }

    /// Interpolate `input` (DECIMATED_LEN Q31 samples) to FRAME_SAMPLES Q31.
    fn process(
        &mut self,
        input:  &[i32; DECIMATED_LEN],
        output: &mut [i32; FRAME_SAMPLES],
    ) {
        // Working buffer: [old state (5) || new input (10)] = 15 samples.
        let mut buf = [0i32; FIR_SUBFILTER_TAPS - 1 + DECIMATED_LEN];
        buf[..FIR_SUBFILTER_TAPS - 1].copy_from_slice(&self.state);
        buf[FIR_SUBFILTER_TAPS - 1..].copy_from_slice(input);

        for k in 0..DECIMATED_LEN {
            for p in 0..FIR_DECIMATE_FACTOR {
                // i64 accumulator for the 6-tap polyphase dot product.
                let mut acc = 0i64;
                for n in 0..FIR_SUBFILTER_TAPS {
                    // Prototype tap for phase p, sub-tap n (time-reversed):
                    // coeff = FIR_COEFFS_Q31[N−1 − p − n×L]
                    let c = FIR_COEFFS_Q31[FIR_NUM_TAPS - 1 - p - n * FIR_DECIMATE_FACTOR];
                    let x = buf[k + FIR_SUBFILTER_TAPS - 1 - n];
                    acc += c as i64 * x as i64;
                }
                // Scale by L=10 (compensate zero-insertion), then Q2.62 → Q1.31.
                let scaled = acc * FIR_DECIMATE_FACTOR as i64;
                output[k * FIR_DECIMATE_FACTOR + p] =
                    (scaled >> 31).clamp(i32::MIN as i64, i32::MAX as i64) as i32;
            }
        }

        // Save the last FIR_SUBFILTER_TAPS−1 input samples as new state.
        self.state.copy_from_slice(&buf[DECIMATED_LEN..]);
    }
}

